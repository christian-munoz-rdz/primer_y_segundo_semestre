ORG 0100H           ;CS:IP
                    
MOV AH, 0           ;CONFIGURAR MODO GRAFICO
MOV AL, 13H         ;320X200 PX, 256 COLORES
INT 10H             ;INTERRUPCION 10

;DIBUJAR LA C                    
MOV AH, 0CH         ;CONFGURA PARA UN SOLO PIXEL
MOV AL, 0FH         ;COLOR BLANCO
MOV CX, 10          ;COORDENADA DE COLUMNA
MOV DX, 10          ;COORDENADA DE FILA
INT 10H

COLUMNA1C:
	INC DX          ;INCREMENTA LA COORDENADA
	INT 10H			;SETEA COLOR DE PIXEL
